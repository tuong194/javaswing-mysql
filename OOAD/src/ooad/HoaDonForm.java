    // Tính tổng tiền sản phẩm
    private void btnTinhtienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTinhtienMouseClicked
       float don_gia = Float.parseFloat(btnGetGiasp.getText());
       int soluong = Integer.parseInt(txtSoluong.getText());
       float tongtien = 0;
       String cbb = (String) cbbGiamgia.getSelectedItem();
        if (cbb=="0") {
            tongtien = don_gia * soluong;
        }else if (cbb == "30%") {
            tongtien = don_gia * soluong * 7/10;
        }else if (cbb == "50%") {
            tongtien = don_gia * soluong/2;
        }
        btnTinhtien.setText(String.valueOf(tongtien));
        
    }//GEN-LAST:event_btnTinhtienMouseClicked

