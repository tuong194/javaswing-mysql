    public boolean updateSoluongSanPham() throws SQLException{
        int numNew = Integer.parseInt(txtSoluong.getText());
        int numOld = 0;
        Connection con = getConnection();
        PreparedStatement ps = con.prepareStatement("SELECT * FROM sanpham WHERE ma_sp='"+txtMasp.getText()+"'");            
        ResultSet rs = ps.executeQuery();
        if(rs.next()){
            numOld = Integer.parseInt( rs.getString("so_luong"));
        }else{
            JOptionPane.showMessageDialog(null, "Mã sản phẩm không tồn tại");
        }
        
        if ((numOld- numNew) >= 0) {
            PreparedStatement ps2 = con.prepareStatement("UPDATE sanpham SET so_luong = '"+(numOld-numNew)+"' WHERE ma_sp='"+txtMasp.getText()+"'");
            ps2.executeUpdate();
            return true;
        
        }else if((numOld - numNew) < 0){
            return false;
        }
        return false;
    }
    
    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        if (txtMahd != null && txtMasp.getText() != null && txtSoluong.getText() != null) {
            try {
                if (updateSoluongSanPham()) {
                    Connection con = getConnection();
                    PreparedStatement ps = con.prepareStatement("INSERT INTO hoadon(ma_hd, ma_sp, gia_sp, so_luong, tong_tien,ngay_ban) VALUES (?,?,?,?,?,?)");
                    ps.setString(1, txtMahd.getText());
                    ps.setString(2, txtMasp.getText());
                    ps.setString(3, btnGetGiasp.getText());
                    ps.setString(4, txtSoluong.getText());
                    ps.setString(5, btnTinhtien.getText());
                    
                    SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                    String date = dateFormat.format(txtNgayban.getDate());
                    ps.setString(6, date);
                
                    ps.executeUpdate();
                    show_jtable_hoadon();
                
                    JOptionPane.showMessageDialog(null, "Xong");
                }else{
                    JOptionPane.showMessageDialog(null, "Số lượng sản phẩm trong cửa hàng không đủ\n Vui lòng xem lại");
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng điền đủ thông tin");
        }
    }//GEN-LAST:event_btnThemMouseClicked

